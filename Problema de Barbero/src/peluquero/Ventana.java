    private void InicioButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InicioButtonActionPerformed
        // TODO add your handling code here:
        
        //numero de sillas
        try{
            Integer.parseInt(nChairsTextBox.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid number");
            return;
        }
        
        final int numeroSillas  = Integer.parseInt(nChairsTextBox.getText());
      
        //Monitor
        final control m = new control(numeroSillas, estadobarbero, model, estadosilla, progressCut);
        
        //Barbero
        final Barbero barbero = new Barbero(m, estadobarbero, estadosilla, progressCut);
        
        List<Cliente> clientes = new ArrayList<Cliente>();
        
        
        //se inicializan los hilso del monitor y del barbero.
        new Thread(barbero).start();
        new Thread(m).start();
        
        //habilitar los botones
        InicioButton.setEnabled(false);
        FinalizarButton.setEnabled(true);
        
        //inicializar clientes
        new Thread(new Runnable() {

            @Override
            public void run() {
                try {
                    int numCliente=0;
                    Random r = new Random();
                    while(true){
                        Thread.sleep(r.nextInt(10000));
                        if(m.clientesEspera<numeroSillas){
                            Cliente nuevo = new Cliente(m, barbero, model, estadobarbero, ++numCliente);
                            Thread t = new Thread(nuevo);
                            t.start();
                        }else{
                            System.out.println("Llego un cliente y se fue");
                        }
                    }
                } catch (InterruptedException ex) {
                    Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }).start(); 
    }//GEN-LAST:event_InicioButtonActionPerformed

